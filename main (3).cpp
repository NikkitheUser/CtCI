#include <iostream>
#include <limits.h>
using namespace std;
int isMax(int a, int b) { 
    int c = a - b; //we subtract a-b to get out int c
    int k = (c >> ((sizeof(int) * CHAR_BIT) - 1)) & 0x1; 
  
    int max = a - k * c; //max is then equal to our first int minus k*c
    return max;
}
int main() {
  int num1,num2;
  cout << "lets find the max! give me a number"<<endl;
  cin>>num1;
  cout<<"now another number!"<<endl;
  cin>>num2;
cout<<isMax(num1, num2)<<" is the bigger number"<<endl;
}